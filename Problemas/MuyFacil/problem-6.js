// Área de un triángulo
// Escribe una función que tome la base y la altura de un triángulo y returnsu área.

// Ejemplos
// triArea(3, 2) ➞ 3
// triArea(7, 4) ➞ 14
// triArea(10, 10) ➞ 50

// Notas
// El área de un triángulo es: (base * height) / 2
// No olvide returnel resultado.

//SOLUCION

const triArea = (base, height) => { return (base * height) / 2 }// return 14
triArea(7, 4);//Paramentro que resive la funcion